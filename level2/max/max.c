#include<stdio.h>

int		max(int* tab, unsigned int len)
{
    int i = -1;
    int l;

    while(i < l - 1)
    {
        if(tab[i] < tab[i + 1])
        l = tab[i];
        tab[i] = tab[i + 1];
        tab[i + 1] = l;
        i = -1;
    }
    i++;
    return 1;
}
