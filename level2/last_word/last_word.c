#include <unistd.h>

int main(int ac, char **av)
{
    int i = 0;
    char *s1 = av[1];
    
    if (ac == 2)
    {
        while (s1[i] != '\0')
        {
            i++;
        }
        i--;
        while (i > 0 && (s1[i] == ' ' || s1[i] == '\t'))
        {
            i--;
        }

        while(i > 0 && (s1[i] != ' ' && s1[i] != '\t'))
        {
            i--;
        }
        while (s1[i])
        {
            if (s1[i] != ' ' && s1[i] != '\t')
               write(1, &s1[i], 1);
            i++;
        }
    }
    write(1,"\n",1);
    return (0);
}
